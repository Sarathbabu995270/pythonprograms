# Arithmetic Operators
a = 10
b = 3
print("Addition:", a + b)          # Addition
print("Subtraction:", a - b)       # Subtraction
print("Multiplication:", a * b)    # Multiplication
print("Division:", a / b)          # Division
print("Modulus:", a % b)           # Modulus
print("Exponentiation:", a ** b)   # Exponentiation
print("Floor Division:", a // b)   # Floor Division

# Comparison Operators
print("Equal:", a == b)            # Equal
print("Not Equal:", a != b)        # Not Equal
print("Greater than:", a > b)      # Greater than
print("Less than:", a < b)         # Less than
print("Greater or Equal:", a >= b) # Greater or Equal
print("Less or Equal:", a <= b)    # Less or Equal

# Logical Operators
x = True
y = False
print("Logical AND:", x and y)     # Logical AND
print("Logical OR:", x or y)       # Logical OR
print("Logical NOT:", not x)       # Logical NOT

# Bitwise Operators
m = 5  # Binary: 0101
n = 3  # Binary: 0011
print("Bitwise AND:", m & n)       # Bitwise AND
print("Bitwise OR:", m | n)        # Bitwise OR
print("Bitwise XOR:", m ^ n)       # Bitwise XOR
print("Bitwise NOT:", ~m)          # Bitwise NOT
print("Left Shift:", m << 1)       # Left Shift
print("Right Shift:", m >> 1)      # Right Shift

# Assignment Operators
p = 10
p += 5  # Equivalent to p = p + 5
print("After +=:", p)
p *= 2  # Equivalent to p = p * 2
print("After *=:", p)

# Membership Operators
lst = [1, 2, 3, 4, 5]
print("Is 3 in list?", 3 in lst)   # Membership (in)
print("Is 6 not in list?", 6 not in lst)  # Membership (not in)

# Identity Operators
a = 10
b = 10
print("Is a identical to b?", a is b)       # Identity (is)
print("Is a not identical to b?", a is not b)  # Identity (is not)

